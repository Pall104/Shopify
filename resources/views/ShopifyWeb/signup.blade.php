                <h3 class="text-center"><strong>Here's a time line about Hawkings:</strong></h3>
                <ul>
                    <li> <strong>1942</strong> : Born in England.</li>
                    <li> <strong>1966–1975</strong> : Hawking extended the singularity theorem concepts first explored in his doctoral thesis. This included not only the existence of singularities but also the theory that the universe might have started as a singularity. Their joint essay was the runner-up in the 1968 Gravity Research Foundation competition.In 1970 they published a proof that if the universe obeys the general theory of relativity and fits any of the models of physical cosmology developed by Alexander Friedmann, then it must have begun as a singularity.</li>
                    <li> <strong>1975-1990</strong> : Hawking returned to Cambridge in 1975 to a more academically senior post, as reader in gravitational physics. The mid to late 1970s were a period of growing public interest in black holes and the physicists who were studying them. Hawking was regularly interviewed for print and television.He also received increasing academic recognition of his work.In 1975, he was awarded both the Eddington Medal and the Pius XI Gold Medal, and in 1976 the Dannie Heineman Prize, the Maxwell Prize and the Hughes Medal.</li>
                    <li> <strong>1990-2000</strong> : Hawking pursued his work in physics: in 1993 he co-edited a book on Euclidean quantum gravity with Gary Gibbons and published a collected edition of his own articles on black holes and the Big Bang. In 1994, at Cambridge's Newton Institute, Hawking and Penrose delivered a series of six lectures that were published in 1996 as "The Nature of Space and Time". In 1997, he conceded a 1991 public scientific wager made with Kip Thorne and John Preskill of Caltech. Hawking had bet that Penrose's proposal of a "cosmic censorship conjecture" – that there could be no "naked singularities" unclothed within a horizon – was correct.After discovering his concession might have been premature, a new and more refined wager was made. This one specified that such singularities would occur without extra conditions.The same year, Thorne, Hawking and Preskill made another bet, this time concerning the black hole information paradox."</li>
                    <li> <strong>2000–2017</strong> : Hawking continued his writings for a popular audience, publishing The Universe in a Nutshell in 2001, and A Briefer History of Time, which he wrote in 2005 with Leonard Mlodinow to update his earlier works with the aim of making them accessible to a wider audience, and God Created the Integers, which appeared in 2006. Along with Thomas Hertog at CERN and Jim Hartle, from 2006 on Hawking developed a theory of "top-down cosmology", which says that the universe had not one unique initial state but many different ones, and therefore that it is inappropriate to formulate a theory that predicts the universe's current configuration from one particular initial state.In July 2017, Hawking was awarded an Honorary Doctorate from Imperial College London.</li>
